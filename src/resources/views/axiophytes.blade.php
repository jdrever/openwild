    <h1>What Are Axiophytes?</h1>
    <p class="lead">Axiophytes are plants that are considered a good sign of a thriving local environment.
    When the axiophytes are doing well, the habitat is doing well. When the axiophytes struggle, people start asking questions.
    In conservation areas, these ‘worthy plants’ become bright beacons among the dull chaff of other species, showing us what is declining where. They are way-markers, or benchmarks. </p>

